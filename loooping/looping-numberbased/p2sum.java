
import java.util.*;
public class p2sum 
{
    public static void main(String[] args)
    {
        Scanner rs=new Scanner(System.in);
        int js=rs.nextInt();
        int sum=0;
        for(int i=0;i<=js;i++)
        {
            sum+=i;
        }
        System .out.println(sum);
    }
}
